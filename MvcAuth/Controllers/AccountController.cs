        // POST: /Account/Login
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Login(LoginViewModel model, string returnUrl)
        {

            if (!ModelState.IsValid)
            {
                return View(model);
            }
            var admin = Db.AdminTbls.Where(x => x.Admin_Email == model.Email && x.Admin_Pass == model.Password).FirstOrDefault();
            if (admin != null)
            {
                Session["AdminID"] = admin.Admin_ID;
                Session["AdminName"] = admin.Admin_Name;
                Session["AdminEmail"] = admin.Admin_Email;

            }

            var professor = Db.ProfessorTbls.Where(x => x.Prof_Email == model.Email && x.Prof_Pass == model.Password).FirstOrDefault();
            if (professor != null)
            {
                Session["ProfessorID"] = professor.Prof_ID;
                Session["ProfessorName"] = professor.Prof_Name;
                Session["ProfessorEmail"] = professor.Prof_Email;
                Session["ProfessorName2"] = Session["ProfessorName"];

            }

            var student = Db.StudentTbls.Where(x => x.St_Email == model.Email && x.St_Password == model.Password).FirstOrDefault();
            if (student != null)
            {
                string name = student.St_Name;
                Session["StudentID"] = student.St_ID;
                Session["StudentName"] = student.St_Name;
                Session["StudentEmail"] = student.St_Email;
            }

            // This doesn't count login failures towards account lockout
            // To enable password failures to trigger account lockout, change to shouldLockout: true
            var result = await SignInManager.PasswordSignInAsync(model.Email, model.Password, model.RememberMe, shouldLockout: false);
            switch (result)
            {
                case SignInStatus.Success:
                    {

                        if (Session["ProfessorID"] != null)
                        {
                            return RedirectToAction("Navigation", "Professor");
                        }
                        else if (Session["AdminID"] != null)
                        {
                            return RedirectToAction("Navigation", "Admin");
                        }
                        else if (Session["StudentID"] != null)
                        {
                            return RedirectToAction("Navigation", "Students");
                        }
                        return View();
                    }

                case SignInStatus.LockedOut:
                    return View("Lockout");
                case SignInStatus.RequiresVerification:
                    return RedirectToAction("SendCode", new { ReturnUrl = returnUrl, RememberMe = model.RememberMe });
                case SignInStatus.Failure:
                default:
                    ModelState.AddModelError("", "Invalid login attempt.");
                    return View(model);
            }
        }
